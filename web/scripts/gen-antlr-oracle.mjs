// Regenerate the ANTLR conformance oracle bundle.
//
// WHAT: bundles the renderer's authoritative ANTLR parser layer
// (zenuml-core-25/src/parser) into a single self-contained ES module,
// src/editor/conformance/antlr-oracle.generated.mjs, with `antlr4` left
// external (web/ already depends on it, 4.11.0 — the shape the generated parser
// needs). The two trivial ramda deep-imports StringUtil.ts uses are shimmed.
//
// WHY: this is the TEST-TIME ORACLE for the editor parser's semantic
// non-conflict gate (see docs/adr/0002). CI checks out web-sequence ALONE —
// zenuml-core-25 is a sibling repo not present in CI — so the oracle must be a
// committed artifact, not built at test time.
//
// WHEN TO RERUN: only when the renderer's ANTLR grammar / parser layer changes
// (rare). The corpus can grow freely WITHOUT rerunning — the bundle computes the
// oracle live for any input. Run from web/:  node scripts/gen-antlr-oracle.mjs
//
// REQUIRES: zenuml-core-25 checked out as a sibling of web-sequence, with its
// deps installed. Fails loudly otherwise.

import { build } from 'vite'
import { existsSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const WEB = resolve(__dirname, '..')

// zenuml-core-25 sits two levels up from web/ (…/zenuml/zenuml-core-25).
const CORE_ROOT = resolve(WEB, '../../zenuml-core-25')
const CORE_SRC = resolve(CORE_ROOT, 'src')
const ENTRY = resolve(CORE_SRC, 'parser/index.js')

if (!existsSync(ENTRY)) {
  console.error(
    `[gen-antlr-oracle] cannot find the core parser at:\n  ${ENTRY}\n` +
      `Check out mermaid-js/zenuml-core as a sibling (zenuml-core-25) and install its deps.`,
  )
  process.exit(1)
}

const OUT_DIR = resolve(WEB, 'src/editor/conformance')

await build({
  configFile: false,
  logLevel: 'warn',
  root: CORE_ROOT,
  resolve: {
    alias: {
      '@': CORE_SRC,
      'ramda/src/pipe': resolve(__dirname, 'ramda-shims/pipe.mjs'),
      'ramda/src/replace': resolve(__dirname, 'ramda-shims/replace.mjs'),
    },
  },
  build: {
    lib: { entry: ENTRY, formats: ['es'], fileName: () => 'antlr-oracle.generated.mjs' },
    outDir: OUT_DIR,
    emptyOutDir: false, // OUT_DIR holds hand-written corpus/test files too.
    rollupOptions: {
      external: ['antlr4'],
      output: {
        banner:
          '// @' + 'generated by scripts/gen-antlr-oracle.mjs — DO NOT EDIT BY HAND.\n' +
          '// This is the renderer\'s ANTLR parser (zenuml-core-25/src/parser) bundled as the\n' +
          '// test-time conformance oracle. Rerun the script if the ANTLR grammar changes.\n' +
          '/* eslint-disable */',
      },
    },
    minify: false,
  },
})

console.log(`[gen-antlr-oracle] wrote ${OUT_DIR}/antlr-oracle.generated.mjs`)
